package com.example.user.service.repository;

import com.example.user.service.entity.UserEntity;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.JpaSpecificationExecutor;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

import java.util.Optional;

@Repository
public interface UserRepository extends JpaRepository<UserEntity, Long>, JpaSpecificationExecutor<UserEntity>
{
	@Query(value = "SELECT u FROM UserEntity u WHERE u.userId = :userId")
	Optional<UserEntity> findByUserId(@Param("userId") long userId);
}
